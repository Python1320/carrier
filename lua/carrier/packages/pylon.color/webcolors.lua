local normalizedNames = {}
for colorName, _ in pairs(WebColors.InitializeColors({}, function() return 0 end)) do
	normalizedNames[string.lower(colorName)] = colorName
end

function WebColors.Initialize(destinationTable, fromRGBA8888)
	WebColors.InitializeColors(destinationTable, fromRGBA8888)
	
	function destinationTable.FromName(colorName)
		return destinationTable[normalizedNames[string.lower(colorName)]]
	end
	
	local colorNames = {}
	for _, colorName in pairs(normalizedNames) do
		colorNames[destinationTable[colorName]] = colorName
	end
	
	function destinationTable.GetName(color)
		return colorNames[color]
	end
	
	return destinationTable
end
